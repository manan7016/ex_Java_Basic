public class Main {
    public static void main(String[] args) {

        System.out.println("Hello World");
        System.out.println();
        int rows = 5;
        for (int i = 1; i <= rows; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
        System.out.println();
        System.out.println("Thank You!");
    }
}